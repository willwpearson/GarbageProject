package garbage.controller;

public class GarbageController
{
	public void start()
	{
		System.out.println("Ayyyy lmao.");
		System.out.println("This project is dope.");
	}
	
	public void loop()
	{
	
		int count = 0;
		while (count < 100)
		{
		System.out.println("xD");
		count ++;
		}	
		while (count < 200)
		{
			System.out.println("lol");
			count ++;
		}
		for(int lol = 0; lol < 10; lol ++)
		{
			System.out.println("xD this is loop # " + (lol + 1) + " out of 10.");
		}
		for(int xD = 0; xD < 200; xD += 10)
		{
			System.out.println("Hola");
		}
	}
}